<template>
  <div class="input-task">
    <form name="form">
      <input
        v-model="someTask"
        type="text"
        name="textview"
        placeholder="add to the list"
        class="input"
        @keypress.enter.prevent="addTask"
      >
    </form>
    <div class="button">
      <input
        type="button"
        value="OK"
        @click="addTask"
      >
    </div>
  </div>
</template>

<script>
export default {
  name: 'InputComponent',
  data: () => ({
    someTask: '',
  }),
  methods: {
    addTask() {
      this.$emit('addTask', this.someTask);
      this.someTask = '';
    },
  },
};
</script>

